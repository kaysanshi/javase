package com.java1995;

public class TestArray {
	public static void main(String[] args) {
		//声明各个类型的数组
		byte[] byteArray=new byte[1];
		char[] charArray=new char[1];
		int[] intArray=new int[1];
		long[] longArray=new long[1];
		float[] floatArray=new float[1];
		double[] doubleArray=new double[1];
		String[] stringArray=new String[1];
		//打印各个类型的数组的初始化值
		System.out.println("byte类型初始化值为"+byteArray[0]);
		System.out.println("char类型初始化值为"+charArray[0]);
		System.out.println("int类型初始化值为"+intArray[0]);
		System.out.println("long类型初始化值为"+longArray[0]);
		System.out.println("float类型初始化值为"+floatArray[0]);
		System.out.println("double类型初始化值为"+doubleArray[0]);
		System.out.println("String类型初始化值为"+stringArray[0]);
		
	}

}
